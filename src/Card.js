export default class Card{
    constructor(identifier, attack, defense, mana, taunt, lifesteal, charge, card_type){
        this.identifier = identifier;
        this.attack = attack;
        this.defense = defense;
        this.mana = mana;
        this.taunt = taunt;
        this.lifesteal = lifesteal;
        this.charge = charge;
        this.card_type = card_type;
        
    }

}